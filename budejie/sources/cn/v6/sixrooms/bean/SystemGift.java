package cn.v6.sixrooms.bean;

public class SystemGift {
    protected int item;
    protected int num;
    protected int rid;
    protected String talias;
    protected int uid;

    public int getUid() {
        return this.uid;
    }

    public void setUid(int i) {
        this.uid = i;
    }

    public int getRid() {
        return this.rid;
    }

    public void setRid(int i) {
        this.rid = i;
    }

    public String getTalias() {
        return this.talias;
    }

    public void setTalias(String str) {
        this.talias = str;
    }

    public int getItem() {
        return this.item;
    }

    public void setItem(int i) {
        this.item = i;
    }

    public int getNum() {
        return this.num;
    }

    public void setNum(int i) {
        this.num = i;
    }
}
