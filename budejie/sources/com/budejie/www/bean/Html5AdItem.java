package com.budejie.www.bean;

public class Html5AdItem {
    private String appendParams;
    private String icon;
    private String id;
    private String name;
    private String safari;
    private String url;

    public String getId() {
        return this.id;
    }

    public void setId(String str) {
        this.id = str;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String str) {
        this.name = str;
    }

    public String getIcon() {
        return this.icon;
    }

    public void setIcon(String str) {
        this.icon = str;
    }

    public String getUrl() {
        return this.url;
    }

    public void setUrl(String str) {
        this.url = str;
    }

    public String getSafari() {
        return this.safari;
    }

    public void setSafari(String str) {
        this.safari = str;
    }

    public String getAppendParams() {
        return this.appendParams;
    }

    public void setAppendParams(String str) {
        this.appendParams = str;
    }
}
